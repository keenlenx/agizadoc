    <section>
        <h2>2. What Happens When You Delete Your Account</h2>
        <p>Upon confirmation of your request:</p>
        <ul>
            <li>Your account and associated data will be permanently deleted, including:
                <ul>
                    <li>Personal information (e.g., name, email, phone number, address).</li>
                    <li>Account preferences and settings.</li>
                    <li>Order history and associated records.</li>
                </ul>
            </li>
            <li>Data required to comply with legal obligations (e.g., tax records, invoices) will be retained for the period mandated by law, as per <strong>Finnish Data Protection Law</strong> and <strong>GDPR</strong>.</li>
            <li>Once deleted, your account cannot be recovered.</li>
        </ul>
    </section>

    <section>
        <h2>3. Processing Time</h2>
        <p>We aim to process account deletion requests within <strong>30 days</strong>. If your request involves complex data retrieval or legal considerations, processing may take longer. We will notify you of any delays.</p>
    </section>

    <section>
        <h2>4. Conditions for Denying Deletion</h2>
        <p>Your account deletion request may be denied if:</p>
        <ul>
            <li>It conflicts with legal or regulatory requirements (e.g., open disputes, outstanding debts).</li>
            <li>It involves data necessary for fraud prevention or security purposes.</li>
        </ul>
    </section>

    <section>
        <h2>5. Data Retention After Deletion</h2>
        <p>Some information may be retained for legal, regulatory, or business reasons, including:</p>
        <ul>
            <li>Records related to financial transactions.</li>
            <li>Data necessary for dispute resolution or legal compliance.</li>
        </ul>
        <p>This retained data will be securely stored and only accessed when legally required.</p>
    </section>

