    <section>
        <h2>1. Requesting Account Deletion</h2>
        <p>You can request the deletion of your account at any time by following these steps:</p>
        <ol>
            <li>Log in to your account.</li>
            <li>Navigate to the <strong>Account Settings</strong> section.</li>
            <li>Select the <strong>Delete Account</strong> option and follow the prompts.</li>
            <li>Confirm your deletion request via the confirmation link sent to your registered email address.</li>
        </ol>
        <p>If you need assistance, contact our support team:</p>
        <ul>
            <li>Email: <a href="mailto:info@agiza.fi">info@agiza.fi</a></li>
